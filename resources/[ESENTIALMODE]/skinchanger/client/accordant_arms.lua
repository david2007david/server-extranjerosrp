function getAccordantArms(sex, torso)

	if ACCORDANT_ARMS == nil then
		init_accordant_arms()
	end

	if sex ~= 0 and sex ~= 1 then
		TriggerEvent('chat:addMessage', { args = { '^1SYSTEM', 'Wrong sex value' } })
		return nil
	end

	return ACCORDANT_ARMS[sex][torso]
end
